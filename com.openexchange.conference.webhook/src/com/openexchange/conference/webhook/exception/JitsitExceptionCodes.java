package com.openexchange.conference.webhook.exception;

import static com.openexchange.conference.webhook.exception.JitsiExceptionMessages.NO_MORE_THAN_ONE_YEAR_IN_ADVANCE_MSG;
import static com.openexchange.exception.OXExceptionStrings.MESSAGE;
import com.openexchange.exception.Category;
import com.openexchange.exception.DisplayableOXExceptionCode;
import com.openexchange.exception.OXException;
import com.openexchange.exception.OXExceptionFactory;

/**
 * {@link JitsitExceptionCodes}
 *
 * @author <a href="mailto:tobias.friedrich@open-xchange.com">Tobias Friedrich</a>
 * @since v8.0.0
 */
public enum JitsitExceptionCodes implements DisplayableOXExceptionCode {

    /**
     * <li>Jitsi meetings cannot be scheduled more than one year in advance. Please adjust the appointment time and try again.</li>
     * <li>Can't save meetings more than one year in advance with jitsi meeting [id %1$s]</li>
     */
    NO_MORE_THAN_ONE_YEAR_IN_ADVANCE("Can't save meetings more than one year in advance with jitsi meeting [id %1$s]", NO_MORE_THAN_ONE_YEAR_IN_ADVANCE_MSG, CATEGORY_USER_INPUT, 1),

    ;

    private static final String PREFIX = "JITSI".intern();

    private String message;
    private String displayMessage;
    private Category category;
    private int number;

    private JitsitExceptionCodes(String message, Category category, int number) {
        this(message, null, category, number);
    }

    private JitsitExceptionCodes(String message, String displayMessage, Category category, int number) {
        this.message = message;
        this.displayMessage = null != displayMessage ? displayMessage : MESSAGE;
        this.category = category;
        this.number = number;
    }

    @Override
    public int getNumber() {
        return number;
    }

    @Override
    public String getMessage() {
        return message;
    }

    @Override
    public String getDisplayMessage() {
        return displayMessage;
    }

    @Override
    public String getPrefix() {
        return PREFIX;
    }

    @Override
    public Category getCategory() {
        return category;
    }

    @Override
    public boolean equals(OXException e) {
        return OXExceptionFactory.getInstance().equals(this, e);
    }

    /**
     * Creates a new {@link OXException} instance pre-filled with this code's attributes.
     *
     * @return The newly created {@link OXException} instance
     */
    public OXException create() {
        return OXExceptionFactory.getInstance().create(this);
    }

    /**
     * Creates a new {@link OXException} instance pre-filled with this code's attributes.
     *
     * @param args The message arguments in case of printf-style message
     * @return The newly created {@link OXException} instance
     */
    public OXException create(final Object... args) {
        return OXExceptionFactory.getInstance().create(this, (Throwable) null, args);
    }

    /**
     * Creates a new {@link OXException} instance pre-filled with this code's attributes.
     *
     * @param cause The optional initial cause
     * @param args The message arguments in case of printf-style message
     * @return The newly created {@link OXException} instance
     */
    public OXException create(final Throwable cause, final Object... args) {
        return OXExceptionFactory.getInstance().create(this, cause, args);
    }

}
