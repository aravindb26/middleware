package com.openexchange.drive.client.windows.rest;

import com.google.gson.Gson;

/**
 * {@link DriveManifest} is used by {@link Gson} to create instances from drive client service manifests
 *
 * @author <a href="mailto:ralf.wurm@open-xchange.com">Ralf Wurm</a>
 * @since v8.0.0
 */
public class DriveManifest {

    private String brand;
    private String version;
    private String downloadPath;
    private String upgradeCode;

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }
    
    public String getDownloadPath() {
        return downloadPath;
    }

    
    public void setDownloadPath(String downloadPath) {
        this.downloadPath = downloadPath;
    }

    public String getUpgradeCode() {
        return upgradeCode;
    }

    public void setUpgradeCode(String upgradeCode) {
        this.upgradeCode = upgradeCode;
    }

}
